bool process_record_user(uint16_t keycode, keyrecord_t* record) {
  switch (keycode) {
    case TMUX_P: {
      if (record->event.pressed) {
        SEND_STRING(SS_LCTL("a"));
      }
      return false;
    }
    case TMUX_P_N: {
      if (record->event.pressed) {
        SEND_STRING(SS_LCTL("an") );
      }
      return false;
    }
  }
  return true;
}

